package cs3500.animation.model;

/**
 * Enum to represent the types of shape action there can be, MOVE represents an action where
 * the shape moves from an initial starting point to a different ending point, while STAY
 * represents an action where the shape stays in the same location for the entirety of the
 * action duration.
 */
public enum ActionType {
  MOVE, STAY;
}
