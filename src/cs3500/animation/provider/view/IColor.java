package cs3500.animation.provider.view;

/**
 * Interface representing out provider's implementation of a color.
 */
public interface IColor {

  /**
   * Returns the red value of this color.
   *
   * @return the integer red value.
   */
  int getRed();

  /**
   * Returns the green value of this color.
   *
   * @return the integer green value.
   */
  int getGreen();

  /**
   * Returns the blue value of this color.
   *
   * @return the integer blue value of this color.
   */
  int getBlue();
}
